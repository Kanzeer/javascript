//Questão 18: Como você pode acessar o valor de uma propriedade de um objeto em JavaScript?

const obj = {
    nome: "Laika", //"nome" sendo nossa propriedade e "Laika" o valor atribuido na propriedade
    idade: 2
};

console.log(obj); //obter toda "const obj"
console.log(obj.idade); //obter uma propriedade especifica